var el = {};
var THEME_KEY = "lakshya-theme";

document.addEventListener("DOMContentLoaded", function () {
  cacheElements();
  renderHeader();
  renderCourses();
  setupTheme();
  bindEvents();
  setupCursorTrail();
  setupContentRestrictions();
});

function cacheElements() {
  el.instituteTitle = document.getElementById("institute-title");
  el.themeToggle = document.getElementById("theme-toggle");
  el.courseList = document.getElementById("course-list");
  el.courseModal = document.getElementById("course-modal");
  el.modalBackdrop = document.getElementById("modal-backdrop");
  el.modalClose = document.getElementById("modal-close");
  el.modalCode = document.getElementById("modal-code");
  el.modalTitle = document.getElementById("modal-title");
  el.modalMeta = document.getElementById("modal-meta");
  el.modalDetails = document.getElementById("modal-details");
  el.modalSyllabus = document.getElementById("modal-syllabus");
}

function renderHeader() {
  if (el.instituteTitle) {
    el.instituteTitle.textContent = brochureData.instituteName || "";
  }
}

function renderCourses() {
  if (!el.courseList) {
    return;
  }

  el.courseList.innerHTML = "";

  var courses = brochureData.courses || [];
  var i;
  for (i = 0; i < courses.length; i += 1) {
    var course = courses[i];
    var card = document.createElement("button");
    card.type = "button";
    card.className = "course-card";
    card.setAttribute("data-course-id", course.id);

    var topRow = document.createElement("div");
    topRow.className = "course-top";

    var code = document.createElement("p");
    code.className = "course-code";
    code.textContent = course.code || "";

    var icon = document.createElement("span");
    icon.className = "course-icon";
    icon.setAttribute("aria-hidden", "true");

    topRow.appendChild(code);
    topRow.appendChild(icon);

    var name = document.createElement("h3");
    name.className = "course-name";
    name.textContent = course.name || "";

    var meta = document.createElement("div");
    meta.className = "course-meta";
    if (course.duration) {
      meta.appendChild(createMetaPill(course.duration));
    }
    if (course.fee) {
      meta.appendChild(createMetaPill(course.fee));
    }

    card.appendChild(topRow);
    card.appendChild(name);
    if (meta.childElementCount > 0) {
      card.appendChild(meta);
    }

    el.courseList.appendChild(card);
  }
}

function bindEvents() {
  if (el.themeToggle) {
    el.themeToggle.addEventListener("click", function () {
      var current = document.documentElement.getAttribute("data-theme") || "light";
      var next = current === "dark" ? "light" : "dark";
      applyTheme(next);
    });
  }

  el.courseList.addEventListener("click", function (event) {
    var trigger = closestByClass(event.target, "course-card");
    if (!trigger) {
      return;
    }

    var courseId = trigger.getAttribute("data-course-id");
    openCourseModal(courseId);
  });

  el.modalClose.addEventListener("click", function () {
    closeCourseModal();
  });

  el.modalBackdrop.addEventListener("click", function () {
    closeCourseModal();
  });

  document.addEventListener("keydown", function (event) {
    if (event.key === "Escape" && !el.courseModal.hidden) {
      closeCourseModal();
    }
  });
}

function openCourseModal(courseId) {
  var course = getCourseById(courseId);
  if (!course) {
    return;
  }

  renderCourseModal(course);
  el.courseModal.hidden = false;
  el.courseModal.setAttribute("aria-hidden", "false");
  document.body.classList.add("modal-open");
}

function closeCourseModal() {
  el.courseModal.hidden = true;
  el.courseModal.setAttribute("aria-hidden", "true");
  document.body.classList.remove("modal-open");
}

function renderCourseModal(course) {
  el.modalCode.textContent = course.code || "";
  el.modalTitle.textContent = course.name || "";

  clearChildren(el.modalMeta);
  if (course.duration) {
    el.modalMeta.appendChild(createMetaPill(course.duration));
  }
  if (course.fee) {
    el.modalMeta.appendChild(createMetaPill(course.fee));
  }

  clearChildren(el.modalDetails);
  addDetailGroup(el.modalDetails, "Duration", course.duration ? [course.duration] : []);
  addDetailGroup(el.modalDetails, "Fee", course.fee ? [course.fee] : []);
  addDetailGroup(el.modalDetails, "Admission", course.admissionRequirements || []);
  addDetailGroup(el.modalDetails, "Examination", course.examination || []);
  addDetailGroup(el.modalDetails, "Notes", course.notes || []);

  clearChildren(el.modalSyllabus);
  if (course.syllabus && course.syllabus.length > 0) {
    var i;
    for (i = 0; i < course.syllabus.length; i += 1) {
      var li = document.createElement("li");
      li.textContent = course.syllabus[i];
      el.modalSyllabus.appendChild(li);
    }
  }
}

function getCourseById(courseId) {
  var courses = brochureData.courses || [];
  var i;
  for (i = 0; i < courses.length; i += 1) {
    if (courses[i].id === courseId) {
      return courses[i];
    }
  }
  return null;
}

function addDetailGroup(parent, title, items) {
  if (!items || !items.length) {
    return;
  }

  var group = document.createElement("section");
  group.className = "detail-group";

  var heading = document.createElement("h4");
  heading.textContent = title;

  var list = document.createElement("ul");
  var i;
  for (i = 0; i < items.length; i += 1) {
    var li = document.createElement("li");
    li.textContent = items[i];
    list.appendChild(li);
  }

  group.appendChild(heading);
  group.appendChild(list);
  parent.appendChild(group);
}

function clearChildren(node) {
  while (node.firstChild) {
    node.removeChild(node.firstChild);
  }
}

function createMetaPill(text) {
  var pill = document.createElement("span");
  pill.className = "meta-pill";
  pill.textContent = text;
  return pill;
}

function closestByClass(node, className) {
  var current = node;
  while (current && current !== document) {
    if (current.classList && current.classList.contains(className)) {
      return current;
    }
    current = current.parentNode;
  }
  return null;
}







function setupTheme() {
  var stored = localStorage.getItem(THEME_KEY);
  var theme = stored === "dark" ? "dark" : "light";
  applyTheme(theme);
}

function applyTheme(theme) {
  document.documentElement.setAttribute("data-theme", theme);
  localStorage.setItem(THEME_KEY, theme);
  if (el.themeToggle) {
    var isDark = theme === "dark";
    el.themeToggle.setAttribute("aria-checked", isDark ? "true" : "false");
    el.themeToggle.setAttribute("aria-label", isDark ? "Switch to light mode" : "Switch to dark mode");
  }
}

function setupContentRestrictions() {
  document.addEventListener("contextmenu", function (event) {
    event.preventDefault();
  });

  document.addEventListener("selectstart", function (event) {
    event.preventDefault();
  });

  document.addEventListener("copy", function (event) {
    event.preventDefault();
  });

  document.addEventListener("cut", function (event) {
    event.preventDefault();
  });

  document.addEventListener("dragstart", function (event) {
    event.preventDefault();
  });

  document.addEventListener("keydown", function (event) {
    var key = (event.key || "").toLowerCase();
    var withModifier = event.ctrlKey || event.metaKey;

    if (withModifier && (key === "c" || key === "x" || key === "a" || key === "u" || key === "s" || key === "p")) {
      event.preventDefault();
    }

    if (key === "f12") {
      event.preventDefault();
    }
  });
}

function setupCursorTrail() {
  if (!document.body) {
    return;
  }

  if (window.matchMedia && window.matchMedia("(pointer: coarse)").matches) {
    return;
  }

  if (window.matchMedia && window.matchMedia("(prefers-reduced-motion: reduce)").matches) {
    return;
  }

  var layer = document.createElement("div");
  layer.className = "cursor-trail-layer";
  document.body.appendChild(layer);

  var pendingPoint = null;
  var rafId = 0;
  var lastSpawnAt = 0;
  var lastX = null;
  var lastY = null;
  var minDistanceSq = 16;
  var spawnInterval = 16;
  var maxParticles = 80;

  document.addEventListener("pointermove", onPointerMove, { passive: true });
  window.addEventListener("blur", clearTrail);
  document.addEventListener("visibilitychange", function () {
    if (document.hidden) {
      clearTrail();
    }
  });

  function onPointerMove(event) {
    pendingPoint = {
      x: event.clientX,
      y: event.clientY,
      t: now()
    };

    if (!rafId) {
      rafId = requestFrame(flushPoint);
    }
  }

  function flushPoint() {
    rafId = 0;

    if (!pendingPoint) {
      return;
    }

    var point = pendingPoint;
    pendingPoint = null;

    if (lastX !== null && lastY !== null) {
      var dx = point.x - lastX;
      var dy = point.y - lastY;
      if (dx * dx + dy * dy < minDistanceSq) {
        return;
      }
    }

    if (point.t - lastSpawnAt < spawnInterval) {
      return;
    }

    lastSpawnAt = point.t;
    lastX = point.x;
    lastY = point.y;
    spawnParticle(point.x, point.y);
  }

  function spawnParticle(x, y) {
    if (layer.childElementCount >= maxParticles) {
      layer.removeChild(layer.firstChild);
    }

    var particle = document.createElement("span");
    particle.className = "cursor-trail-particle";

    var size = 4 + Math.random() * 7;
    var offsetX = (Math.random() - 0.5) * 10;
    var offsetY = (Math.random() - 0.5) * 10;

    particle.style.width = size.toFixed(2) + "px";
    particle.style.height = size.toFixed(2) + "px";
    particle.style.left = x + offsetX + "px";
    particle.style.top = y + offsetY + "px";
    particle.style.opacity = (0.5 + Math.random() * 0.4).toFixed(2);
    particle.style.animationDuration = (460 + Math.random() * 220).toFixed(0) + "ms";

    layer.appendChild(particle);

    particle.addEventListener("animationend", function () {
      if (particle.parentNode) {
        particle.parentNode.removeChild(particle);
      }
    });
  }

  function clearTrail() {
    pendingPoint = null;
    layer.innerHTML = "";
  }

  function now() {
    if (window.performance && typeof window.performance.now === "function") {
      return window.performance.now();
    }
    return Date.now();
  }

  function requestFrame(callback) {
    if (window.requestAnimationFrame) {
      return window.requestAnimationFrame(callback);
    }
    return window.setTimeout(callback, 16);
  }
}
